<section>
	<h2>History</h2>
	<p>
		Shadow Spear Initiative (SHDWSPR) was founded in 2948. Born from a vision of independence and
		adaptability, SHDWSPR operates in the vast expanse of the 'verse, offering its members freedom
		to pursue their own goals while maintaining the support of a dedicated and skilled organization.
	</p>
	<p>
		SHDWSPR engages in a range of activities, including (but not limited to) freelance contracts, mercenary work, bounty hunting, trading, resource gathering, and hauling.
	</p>
	<p>
		Despite its mercenary roots, SHDWSPR is not bound by a strict military hierarchy. Instead, it
		thrives on a brotherhood of capable pilots, soldiers, and industrialists who work together for
		mutual success.
	</p>
</section>
